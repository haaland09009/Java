package ch02;

public class Sustitute2 {
	public static void main(String[] args) {
		int i1 = 12, i2 = 7;
		i1 += i2;
		System.out.println("i1 = " + i1 + ", i2 = " + i2);
		i1 -= i2;
		System.out.println("i1 = " + i1 + ", i2 = " + i2);
		i1 *= i2;
		System.out.println("i1 = " + i1 + ", i2 = " + i2);
		i1 /= i2;
		System.out.println("i1 = " + i1 + ", i2 = " + i2);
		i1 %= i2;
		System.out.println("i1 = " + i1 + ", i2 = " + i2);
		
	}
}
