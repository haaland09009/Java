package ch02;

public class Var1 {
	public static void main(String[] args) {
		int i1 = 12;
		int i2 = 45;
		System.out.printf("%d + %d = %d\n", i1, i2, i1 + i2);
		System.out.printf("%d = %d = %d\n", i1, i2, i1 - i2);
		System.out.printf("%d * %d = %d\n", i1, i2, i1 * i2);
//		자바에서는 정수끼리 연산은 결과도 정수
		System.out.printf("%d / %d = %d\n", i1, i2, i1 / i2);


		
	}
}
